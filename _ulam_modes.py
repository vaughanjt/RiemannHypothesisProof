"""Multi-mode operator: Ulam spiral quadratic families as independent modes.

THE INSIGHT (from the Ulam spiral image):
  Primes aren't uniformly distributed — they cluster along QUADRATIC
  POLYNOMIAL FAMILIES f(n) = an^2 + bn + c. Each family appears as a
  diagonal direction in the Ulam spiral. There are ~6-8 visible families.

  Our current kernel treats all primes equally:
    K(d,T) = sum_p log(p)/p^{m/2} * cos(2*pi*d*m*log(p)/log(T))

  But primes from DIFFERENT quadratic families contribute differently
  to the pair correlation. Each family is a separate MODE of the operator.

APPROACH:
  1. Classify primes by their quadratic residue class (mod small numbers)
     — this approximates the Ulam spiral directional families
  2. Build a separate kernel for each class
  3. Give each class its own coupling constant
  4. The full off-diagonal: H_{k,k+d} = sum_families C_i * K_i(d, T_k)

The quadratic families in the Ulam spiral correspond to:
  - Primes = 1 (mod 4) vs 3 (mod 4)  [the two main diagonals]
  - Primes = 1,2,3,4,5,6 (mod various) [finer structure]
  - Primes generated by specific quadratics (Euler's n^2+n+41, etc.)

More precisely, the Ulam spiral diagonals are:
  Direction 1: 4n^2 + 0n + c  (horizontal)
  Direction 2: 4n^2 + 2n + c  (diagonal)
  Direction 3: 4n^2 + 4n + c  (vertical, shifted)
  etc.
The key discriminant is b^2 - 16c, and primes cluster where
the class number h(b^2-16c) is small.
"""
