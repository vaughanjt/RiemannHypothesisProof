def classify_primes(primes):
    """Classify primes by residue classes — approximating Ulam families.

    The Ulam spiral's diagonal lines come from primes in specific
    residue classes. The key moduli are:
      mod 4:  two families (1 mod 4, 3 mod 4) — the main diagonals
      mod 6:  two families (1 mod 6, 5 mod 6) — hexagonal structure
      mod 3:  primes = 1 vs 2 mod 3
      mod 8:  four families (1,3,5,7 mod 8) — finer diagonal structure
      mod 12: four families (1,5,7,11) — combines mod 3 and mod 4

    Also classify by quadratic character:
      Primes where -1 is a QR (p = 1 mod 4) — one spiral direction
      Primes where 2 is a QR (p = ±1 mod 8) — another direction
      Primes where 3 is a QR (p = ±1 mod 12) — another direction
    """
    families = {}

    # Mod 4 (the two main Ulam diagonals)
    families["p=1(4)"] = [p for p in primes if p % 4 == 1]
    families["p=3(4)"] = [p for p in primes if p % 4 == 3]

    # Mod 6
    families["p=1(6)"] = [p for p in primes if p % 6 == 1]
    families["p=5(6)"] = [p for p in primes if p % 6 == 5]

    # Mod 8 (four directions)
    families["p=1(8)"] = [p for p in primes if p % 8 == 1]
    families["p=3(8)"] = [p for p in primes if p % 8 == 3]
    families["p=5(8)"] = [p for p in primes if p % 8 == 5]
    families["p=7(8)"] = [p for p in primes if p % 8 == 7]

    # Quadratic residue families
    families["QR(-1)"] = [p for p in primes if p % 4 == 1]  # -1 is QR mod p
    families["QR(2)"] = [p for p in primes if p % 8 in (1, 7)]  # 2 is QR mod p
    families["QR(3)"] = [p for p in primes if p % 12 in (1, 11)]  # 3 is QR mod p
    families["QR(5)"] = [p for p in primes if p % 5 in (1, 4)]  # 5 is QR mod p
    families["QR(-3)"] = [p for p in primes if p % 3 == 1]  # -3 is QR mod p

    # Euler's polynomial family: primes of form n^2 + n + 41
    euler_primes = set()
    for n_val in range(200):
        val = n_val**2 + n_val + 41
        if val in set(primes):
            euler_primes.add(val)
    families["Euler(41)"] = sorted(euler_primes)

    return families


