def kernel_family(d, T, primes_subset, max_m=2):
    """Kernel using only primes from one family."""
    log_T = np.log(max(T, 10) / (2*np.pi))
    if log_T < 0.1:
        log_T = 0.1
    val = 0.0
    for p in primes_subset:
        lp = np.log(p)
        for m in range(1, max_m+1):
            amp = lp / (p**(m/2) * log_T)
            val += amp * np.cos(2 * np.pi * d * m * lp / log_T)
    return val


def build_multimode_banded(alpha_vals, family_dict, bandwidth, coupling_dict):
    """Build banded matrix with separate coupling per family.

    H_{k,k+d} = sum_families C_i * K_i(d, T_k)
    """
    n = len(alpha_vals)
    H = np.diag(alpha_vals.copy())

    for k in range(n):
        T_k = alpha_vals[k]
        for d in range(1, bandwidth + 1):
            if k + d < n:
                val = 0.0
                for fname, plist in family_dict.items():
                    if fname in coupling_dict and len(plist) > 0:
                        val += coupling_dict[fname] * kernel_family(d, T_k, plist)
                H[k, k+d] = val
                H[k+d, k] = val

    return H


